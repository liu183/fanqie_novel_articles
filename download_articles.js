// 清理文件名，移除不允许的字符
function sanitizeFilename(title) {
  return title
    .replace(/[<>:"/\\|?*]/g, '_')  // 替换不允许的字符
    .replace(/\s+/g, '_')            // 空格替换为下划线
    .substring(0, 200);              // 限制长度
}

// 下载单个文章
async function downloadArticle(articleId, title, category) {
  const url = `https://fanqienovel.com/writer/zone/article/${articleId}`;
  const filename = sanitizeFilename(title) + '.md';
  const filepath = path.join(__dirname, category, filename);

  // 检查文件是否已存在
  if (fs.existsSync(filepath)) {
    console.log(`跳过已存在: ${category}/${filename}`);
    return;
  }

  console.log(`下载中: ${category}/${filename}`);

  return new Promise((resolve, reject) => {
    https.get(url, (res) => {
      let data = '';

      res.on('data', (chunk) => {
        data += chunk;
      });

      res.on('end', () => {
        // 尝试提取文章内容
        let content = '';

        // 简单提取标题和链接
        content += `# ${title}\n\n`;
        content += `> 来源: [番茄小说作家课堂](${url})\n\n`;
        content += `---\n\n`;
        content += `*请访问原文链接查看完整内容*\n`;

        // 写入文件
        fs.writeFileSync(filepath, content, 'utf8');
        console.log(`完成: ${category}/${filename}`);
        resolve();
      });
    }).on('error', (err) => {
      console.error(`下载失败: ${category}/${filename}`, err.message);
      // 仍然创建一个占位文件
      const content = `# ${title}\n\n> 来源: [番茄小说作家课堂](${url})\n\n---\n\n*下载失败，请手动访问: ${url}*\n`;
      fs.writeFileSync(filepath, content, 'utf8');
      resolve();
    });
  });
}

// 延迟函数，避免请求过快
function delay(ms) {
  return new Promise(resolve => setTimeout(resolve, ms));
}

// 主函数
async function main() {
  console.log('开始下载文章...\n');

  for (const [category, articleList] of Object.entries(articles)) {
    console.log(`\n处理专区: ${category} (${articleList.length} 篇)`);
    console.log('='.repeat(50));

    for (let i = 0; i < articleList.length; i++) {
      const article = articleList[i];
      await downloadArticle(article.url, article.title, category);

      // 每10篇暂停一下
      if ((i + 1) % 10 === 0) {
        console.log(`\n已处理 ${i + 1}/${articleList.length}，暂停2秒...\n`);
        await delay(2000);
      }

      // 每篇之间延迟
      await delay(500);
    }
  }

  console.log('\n下载完成！');
}

main().catch(console.error);
